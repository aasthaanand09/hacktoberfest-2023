// Destructuring helps us unpack values from arrays and objects and assign them to separate variables in a simple and smooth way.

// With Destructuring
const objects = ['table', 'iPhone', 'apple']
const [furniture, mobile, fruit] = objects

// Without Destructuring
const furniture = objects[0]
const mobile = objects[1]
const fruit = objects[2]

